using NUnit.Framework;
using Moq;
using ReFolder.Dir;
using ReFolder.Dir.Description;
using System;
using System.Collections.Generic;


namespace ReFolder.Tests
{
    class ChildDir_Tests
    {
        [Test]
        public void AutoGenerateDirFullName_Pass_WhenAcceptableParrentIsDelivered()
        {
            //arrange
            string mainAddress = "c:\\blueCat\\MyCats";
            string childDirName = "puszek";
            IEditableDirWithChildren main = Mock.Of<IEditableDirWithChildren>(x=> 
            x.Description.FullName==mainAddress);
            IEditableDirWithChildrenAndParrent childDir = new ChildDir(childDirName, main);
            //act
            var address = childDir.AutoGenerateDirFullName();
            //assert
            Assert.AreEqual($"{mainAddress}\\puszek", address);
        }
        [Test]
        public void AutoGenerateDirFullName_ChangeDirFullName_WhenWhenParrentFullNameChange()
        {
            //arange
            string fullName = "c:\\cats\\RedCats";
            string childDirName = "puszek";
            IEditableDirWithChildren mainDir = new MainDir(new DirDescription("c:\\cats\\BlueCats", "BlueCats"));
            IEditableDirWithChildrenAndParrent childDir = new ChildDir(childDirName, mainDir);
            mainDir.AddChildToChildrenList(childDir);
            mainDir.Description.FullName = fullName;
            //act
            childDir.AutoGenerateDirFullName();
            //assert
            Assert.AreEqual($"{fullName}\\{childDirName}",childDir.Description.FullName);
        }
        [Test]
        public void AutoGenerateDirFullName_ThrowNullReferenceException_WhenParrentFullNameIsNull()
        {
            //arange
            IEditableDirWithChildren mainDir = Mock.Of<IEditableDirWithChildren>(x =>
             x.Description.FullName == null);
            IEditableDirWithChildrenAndParrent childDir = new ChildDir("puszek", mainDir);
            mainDir.AddChildToChildrenList(childDir);
            //act
            TestDelegate action=() => childDir.AutoGenerateDirFullName();
            //assert
            Assert.Throws<NullReferenceException>(action);
        }
    }
}
